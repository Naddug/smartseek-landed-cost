// Update html lang and dir (RTL for Arabic) when language changes
i18n.on("languageChanged", (lng) => {
  const html = document.documentElement;
  const code = lng.split("-")[0];
  html.lang = lng;
  html.dir = code === "ar" ? "rtl" : "ltr";
});

